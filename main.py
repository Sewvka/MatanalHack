def convert_char(c):
    o = ord(c)
    if 0xa0 <= o <= 0xff:
        o -= 0x10
    if 0 <= o < 256:
        return bytearray([o]).decode('iso8859-5')
    else:
        return c


def main():
    l = '_'
    while True:

        data = pyperclip.waitForNewPaste()
        if data == '':
            continue
        if data != l:
            l = data

            outstr = []
            length = len(l)
            for i in range(length):
                if (l[i] == "-" and (i + 1) < length and l[i + 1] == '\r') or (
                        l[i] == '\r' and l[i - 1] == '-' and l[i + 1] == '\n') or (l[i] == '\n' and l[i - 1] == '\r'):
                    continue
                if (l[i] == '\r') or (l[i] == '\n'):
                    outstr.append(' ')
                    continue
                outstr.append(convert_char(l[i]))
            pyperclip.copy(''.join(outstr))
            l = ''.join(outstr)


if __name__ == '__main__':
    main()
